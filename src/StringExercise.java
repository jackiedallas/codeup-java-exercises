public class StringExercise {
    public static void main(String[] args) {
        String noEducation = "We don't need no education";
        String noThoughtCtrl = "We don't need no thought control";
        String checkOut = "Check \"this\" out!, \"s inside of \"s!";
        String mainDrive = "In windows, the main drive is usually C:\\";
        String backSlashes = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";

        System.out.println(noEducation);
        System.out.println(noThoughtCtrl);
        System.out.println(checkOut);
        System.out.println(mainDrive);
        System.out.println(backSlashes);

    }
}
